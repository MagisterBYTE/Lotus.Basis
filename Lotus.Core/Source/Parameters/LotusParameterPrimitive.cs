		/// </summary>
		//-------------------------------------------------------------------------------------------------------------
		[Serializable]
		public class CParameterBool : ParameterItem<Boolean>
		{
			#region ======================================= СВОЙСТВА ==================================================
			//
			// ОСНОВНЫЕ ПАРАМЕТРЫ
			//
			/// <summary>
			/// Тип данных значения
			/// </summary>
			[XmlAttribute]
			public override TParameterValueType ValueType
			{
				get { return TParameterValueType.Boolean; }
			}
			#endregion

			#region ======================================= КОНСТРУКТОРЫ ==============================================
			//---------------------------------------------------------------------------------------------------------
			/// <summary>
			/// Конструктор по умолчанию инициализирует объект класса предустановленными значениями
			/// </summary>
			//---------------------------------------------------------------------------------------------------------
			public CParameterBool()
			{
			}

			//---------------------------------------------------------------------------------------------------------
			/// <summary>
			/// Конструктор инициализирует объект класса указанными параметрами
			/// </summary>
			/// <param name="parameterName">Имя параметра</param>
			/// <param name="value">Значения параметра</param>
			//---------------------------------------------------------------------------------------------------------
			public CParameterBool(String parameterName, Boolean value)
				: base(parameterName)
			{
				_value = value;
			}

			//---------------------------------------------------------------------------------------------------------
			/// <summary>
			/// Конструктор инициализирует объект класса указанными параметрами
			/// </summary>
			/// <param name="id">Идентификатор параметра</param>
			/// <param name="value">Значения параметра</param>
			//---------------------------------------------------------------------------------------------------------
			public CParameterBool(Int32 id, Boolean value)
				: base(id)
			{
				_value = value;
			}
			#endregion

			#region ======================================= ОБЩИЕ МЕТОДЫ ==============================================
			//---------------------------------------------------------------------------------------------------------
			/// <summary>
			/// Запись в строковый поток в формате Json
			/// </summary>
			/// <param name="streamWriter">Строковый поток</param>
			/// <param name="depth">Текущая глубина вложенности</param>
			/// <param name="isArray">Статус массива</param>
			//---------------------------------------------------------------------------------------------------------
			public override void WriteToJson(StreamWriter streamWriter, Int32 depth, Boolean isArray)
			{
				streamWriter.Write(XChar.NewLine);
				streamWriter.Write(XString.Depths[depth]);

				if(isArray == false) 
				{
					streamWriter.Write(XChar.DoubleQuotes);
					streamWriter.Write(Name);
					streamWriter.Write(XChar.DoubleQuotes);

					streamWriter.Write(": ");
				}
				streamWriter.Write(Value);
			}
			#endregion
		}

