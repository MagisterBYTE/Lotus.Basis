using System;
using System.ComponentModel;
//=====================================================================================================================
namespace Lotus
{
	namespace Repository
	{
		//-------------------------------------------------------------------------------------------------------------
		/** \addtogroup RepositoryFilter
		*@{*/
		//-------------------------------------------------------------------------------------------------------------
		/// <summary>
		/// Базовый интерфейс для фильтрации данных
		/// </summary>
		//-------------------------------------------------------------------------------------------------------------
		public class ILotusFilterObject
		{

		}

		//-------------------------------------------------------------------------------------------------------------
		/// <summary>
		/// Универсальный класс для фильтрации данных
		/// </summary>
		//-------------------------------------------------------------------------------------------------------------
		public class CFilterObject : ILotusFilterObject
		{
			#region ======================================= СВОЙСТВА ==================================================
			/// <summary>
			/// Фильтры по свойствам
			/// </summary>
			public CFilterProperty[] Filters { get; set; } = default!;
			#endregion

			#region ======================================= КОНСТРУКТОРЫ ==============================================
			//---------------------------------------------------------------------------------------------------------
			/// <summary>
			/// Конструктор по умолчанию инициализирует объект класса предустановленными значениями
			/// </summary>
			//---------------------------------------------------------------------------------------------------------
			public CFilterObject()
			{
			}

			//---------------------------------------------------------------------------------------------------------
			/// <summary>
			/// Конструктор инициализирует объект класса указанными параметрами
			/// </summary>
			/// <param name="filters">Список фильтров</param>
			//---------------------------------------------------------------------------------------------------------
			public CFilterObject(params CFilterProperty[] filters)
			{
				Filters = filters;
			}
			#endregion
		}
		//-------------------------------------------------------------------------------------------------------------
		/**@}*/
		//-------------------------------------------------------------------------------------------------------------
	}
}
